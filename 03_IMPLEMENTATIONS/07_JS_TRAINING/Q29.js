/**
 * Mastery Drill: No.29 (Advanced: RegExp / 正規表現の基本フラグ)
 * --------------------------------------------------
 * 【シナリオ】
 * 1. 文字列内の「すべての」スラッシュをハイフンに変換せよ。
 * 2. 大文字・小文字を無視してキーワードを置換せよ。
 * --------------------------------------------------
 */

var target = "2026/02/22 (Sunday/Holiday)";

// --- [1. なぜスラッシュ /.../ なのか？] ---
// JSにおいて / で囲まれた部分は「正規表現オブジェクト」という特別な型になる。
// "..." (文字列) で指定すると、最初の1つしか見つけてくれない。

// ❌ 最初の1つだけ置換
console.log("Q29 String: " + target.replace("/", "-")); 
// -> "2026-02/22 (Sunday/Holiday)"

// ✅ 正規表現 /.../g で「すべて」置換
// g = global (全体)
console.log("Q29 Global: " + target.replace(/\//g, "-")); 
// -> "2026-02-22 (Sunday-Holiday)"
// ※ / 自体を探すときは \/ とエスケープが必要。


// --- [2. 大文字小文字を無視する /.../i ] ---
// i = ignore case (大文字小文字を無視)
var msg = "Warning: system error. WARNING!";
console.log("Q29 IgnoreCase: " + msg.replace(/warning/gi, "Alert"));
// -> "Alert: system error. Alert!"

/**
 * 【精読時の解読ガイド】
 * 1. スラッシュで囲まれていたら: 「あ、特殊な検索パターン（正規表現）だな」と即判断。
 * 2. おしりの文字をチェック:
 * - /.../g  -> 「全部やるんだな（Global）」
 * - /.../i  -> 「大文字小文字を気にしないんだな（Ignore Case）」
 * - /.../gi -> 「全部、かつ大文字小文字無視だな」
 * 3. 実務の罠:
 * レガシーコードで `replace` を使っているのに /g がついていない場合、
 * 「2番目以降のデータが置換されずに残っているバグ」が潜んでいる可能性が高いです。
 */