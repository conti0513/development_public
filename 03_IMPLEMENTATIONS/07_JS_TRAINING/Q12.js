// --- [ES5 / ES6 共通の基本形] ---
// JSのオブジェクトは { key: value } の形式（オブジェクトリテラル）。
// PHPの連想配列 array('name' => 'Taro') に非常に近い構造です。
var user = {
    name: 'Taro',
    age: 25,
    isAdmin: false // 末尾カンマはES5以降の標準的な環境で許容されます
};

console.log('User Name: ' + user.name);


/* ▼ 言語別「データ構造」の呼び方と特徴
  -----------------------------------------------------------------------
  | 特徴         | JavaScript     | PHP            | GO (Golang)      |
  | :----------- | :------------- | :------------- | :--------------- |
  | 呼び方       | オブジェクト    | 連想配列        | **Struct (構造体)** |
  | 型の固定     | なし (自由)     | なし (自由)     | **あり (厳格)** |
  | 動的な追加   | 可能 (勝手に変更) | 可能            | **不可能 (定義が必要)** |
  -----------------------------------------------------------------------
*/

/**
 * 【実務の視点：なぜあえて var で書くのか？】
 * 1. 実行環境の制約（ES5互換性）: 
 * 特定のエンタープライズ系クラウド基盤や古いJSエンジン上では、
 * const や let を使用すると構文エラー（Syntax Error）でシステムが停止する恐れがあります。
 * 2. 信頼性への投資: 
 * 「最新の環境でしか動かないコード」ではなく、「制約のある実務環境でも100%確実に動作する」
 * 堅牢なコードをあえて var で記述しています。
 * 3. 精神性はモダン（ES6/GO）:
 * 文法は var ですが、開発者の意識としては const (再代入不可) や let (ブロックスコープ) 
 * のつもりで、意図せぬ書き換えが発生しないよう設計するのがプロの作法です。
 */

/**
 * 【解法ポイント】
 * 1. JSのオブジェクトは設計図（Struct）なしでその場で作成可能。
 * 2. この「ゆるさ」を補うために、Q11で学んだ「ガード句」によるチェックが実務では必須。
 * 3. アクセス方法： `user.name`（ドット記法）のほか、
 * 動的なアクセスには `user['name']`（ブラケット記法）を用います。
 */