/**
 * Mastery Drill: No.06 (Conditional Branching & Truthy/Falsy)
 * --------------------------------------------------
 * 【お題】数値 n が 0 以上なら "正（またはゼロ）" と出力
 * --------------------------------------------------
 */

// --- [パターン1: 数値比較（ES5/ES6/PHP 共通）] ---
var n = 0;
if (n >= 0) {
    console.log('Result: 0以上です');
}

// --- [パターン2: 真偽判定の検証（var / let / const 共通）] ---
// JSにおいて、数値の 0 は宣言方法に関わらず常に "Falsy" (偽とみなされる値) です。
var   v_n = 0;
let   l_n = 0;
const c_n = 0;

if (!v_n && !l_n && !c_n) {
    console.log('JS共通ルール: 数値の 0 は false と判定される');
}

/* ▼ 真偽判定の比較表 (PHP経験者への警告)
 *  -------------------------------------------------------------
 *  | 値     | JavaScript      | PHP             | 備考                |
 *  | :----- | :-------------- | :-------------- | :------------------ |
 *  | 0      | false (Falsy)   | false           | ここは同じ          |
 *  | "0"    | true (Truthy)   | false           | 【激ヤバ】JSは文字ありとみなす |
 *  | []     | true (Truthy)   | false           | 【激ヤバ】JSは箱ありとみなす |
 *  | null   | false (Falsy)   | false           | 共通                |
 *  -------------------------------------------------------------
 */

/**
 * 【解法ポイント】
 * 1. 宣言（var/let/const）に関わらず、JSの型判定ルール（Truthy/Falsy）は一定。
 * 2. PHP脳で最も危険なのは "0" と []。JSではこれらは「中身がある」として true になる。
 * 3. 現場での対策: 
 * - 配列の空判定なら `if (arr.length > 0)` 
 * - 文字列の空判定なら `if (str !== "")` 
 * のように、型に応じたプロパティで判定するのがよい書き方。
 * 1行ハック：「『"0" や [] も True になる』という JS 特有の挙動を前提に、暗黙の判定を捨ててプロパティや比較演算子による『物理的な個数・値の検証』を徹底せよ。」
 */