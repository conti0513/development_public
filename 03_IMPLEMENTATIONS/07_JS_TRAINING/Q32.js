/**
 * Mastery Drill: No.32 (typeof / 変数の存在確認)
 * --------------------------------------------------
 * 【シナリオ】
 * 外部ファイルで定義されているかもしれない変数 `config` が、
 * 今の環境に存在するかどうかを、エラーを出さずに判定せよ。
 * --------------------------------------------------
 */

// もし普通に if (config) と書くと、configが未定義なら「ReferenceError」で即死する。

// --- [基本の形: typeof] ---
// typeof は、対象が未定義（存在しない）でもエラーを吐かず、文字列 "undefined" を返す。
if (typeof config === 'undefined') {
    console.log("Q32: config変数は見つかりませんでした。デフォルト設定を適用します。");
}

// --- [実務の例: プラグインやライブラリの読み込み確認] ---
if (typeof jQuery !== 'undefined') {
    console.log("Q32: jQueryがロード済みです。");
}

