/**
 * Mastery Drill: No.07 (Strict Comparison)
 * --------------------------------------------------
 * 【お題】x が 5 と等しいか判定する
 * --------------------------------------------------
 */

var x = '5'; // 文字列の 5

// --- [JS / PHP: ゆるい判定] ---
if (x == 5) {
    // JS: 「文字列の5も数字の5も、まあ一緒でしょ」と忖度する
    // PHP: 同上
    console.log('JS/PHP (==): true になる（型を無視）');
}

// --- [JS / PHP: 厳密判定] ---
if (x === 5) {
    // 型まで一致しないのでここには入らない
} else {
    console.log('JS/PHP (===): false になる（型が違う）');
}

/* * --- [GO: 厳格（参考）] ---
 * var x string = "5"
 * if x == 5 { ... } 
 * -> !! COMPILATION ERROR !! (mismatched types string and int)
 * GOは「比べること自体がナンセンス」と怒って、実行すらさせてくれない。
 */

/* ▼ 言語別「忖度（そんたく）」度合い表
  -----------------------------------------------------------------------
  | 比較内容      | JS (ES5/6)     | PHP            | GO (Golang)      |
  | :----------- | :------------- | :------------- | :--------------- |
  | "5" == 5     | ✅ true (忖度)  | ✅ true (忖度)  | ❌ コンパイルエラー |
  | 0 == ""      | ✅ true (謎忖度)| ✅ true (忖度)  | ❌ コンパイルエラー |
  | 0 === ""     | ❌ false       | ❌ false       | (常にこれ)        |
  -----------------------------------------------------------------------
*/

/**
 * 【解法ポイント】
 * 1. JSとPHPは「動的型付け」の兄弟。== を使うと裏で勝手に型を変換する。
 * 2. GOは「静的型付け」の番人。型が違うものを比較した瞬間にビルドが止まる。
 * 3. ES5環境のJSを書く時は、「頭の中はGO」でいるのが一番安全。
 * 勝手な忖度をさせないために、常に === を使う。
 */