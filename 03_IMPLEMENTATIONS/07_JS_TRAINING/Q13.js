/**
 * Mastery Drill: No.13 (Truthy & Falsy / 判定の境界線)
 * --------------------------------------------------
 * 【シナリオ】
 * システム設定値(config)を読み込み、値がある場合のみ処理を実行せよ。
 * ただし、「数値の 0」は有効な設定値として扱う必要がある。
 * --------------------------------------------------
 */

var configValue = 0; // 設定値が「0」というケース

// --- [アンチパターン：暗黙の真偽判定] ---
// JSでは 0 は Falsy（偽）とみなされるため、設定があるのに else に飛んでしまう。
if (configValue) {
    console.log("Q13: 設定を適用します");
} else {
    console.log("Q13: 設定がないためスキップします（バグの可能性）");
}

// --- [プロの作法：厳密な比較] ---
// 「値が null または undefined ではないこと」を明示的に判定する。
if (configValue !== null && configValue !== undefined && configValue !== "") {
    console.log("Q13: 正しく設定（0）を検出しました。");
}

