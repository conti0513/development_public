/**
 * Mastery Drill: No.26 (Date Object / 日付と時刻)
 * --------------------------------------------------
 * 【シナリオ】
 * 現在の日時を取得し、「年・月・日」をバラバラに取り出して
 * ログ出力せよ。
 * --------------------------------------------------
 */

var now = new Date();

// --- [基本の取得メソッド] ---
var year  = now.getFullYear(); // 2026
var month = now.getMonth() + 1; // 【超重要】0から始まる(0=1月)ので、必ず +1 する！
var date  = now.getDate();     // 日 (1-31)
var day   = now.getDay();      // 曜日 (0=日, 1=月...)

console.log("Q26 Today: " + year + "/" + month + "/" + date);

// --- [実務でよく使う「エポックミリ秒」] ---
// 1970/1/1 からの経過ミリ秒。日付の比較（どっちが新しいか）に最適。
var timestamp = now.getTime(); 
console.log("Q26 Timestamp: " + timestamp);

