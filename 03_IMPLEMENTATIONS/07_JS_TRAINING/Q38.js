/**
 * Mastery Drill: No.38 (Ternary Operator / 条件演算子)
 * --------------------------------------------------
 * 【シナリオ】
 * 1. ユーザーが管理者(Admin)かどうかで、表示するラベルを1行で切り替えよ。
 * 2. ネスト（二重）三項演算子を解読し、複雑な条件分岐を読み解け。
 * --------------------------------------------------
 */

var userRole = "admin";

// --- [1. 基本形] ---
// (条件) ? 真の場合の値 : 偽の場合の値;
var label = (userRole === "admin") ? "管理者" : "一般ユーザー";

console.log("Q38 Label:", label); // "管理者"


// --- [2. 現場の「気まずい」ネストの解読] ---
// 精読中、以下のようなコードに出会ったら要注意！
var score = 85;
var grade = (score >= 90) ? "A" 
          : (score >= 80) ? "B" 
          : "C";

// 読み解き方：
// もし 90以上なら "A"
// さもなくば (もし 80以上なら "B"、さもなくば "C")
console.log("Q38 Grade:", grade); // "B"

