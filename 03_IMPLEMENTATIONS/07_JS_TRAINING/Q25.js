/**
 * Mastery Drill: No.25 (String Conversion / 明示的な型変換)
 * --------------------------------------------------
 * 【シナリオ】
 * 数値を文字列に変換し、他の文字列（IDの接頭辞など）と安全に結合せよ。
 * --------------------------------------------------
 */

var num = 123;

// --- [方法1: String関数] ---
// 最も安全。もし値が null や undefined でもエラーにならず "null" という文字列を返す。
var s1 = String(num); 

// --- [方法2: .toString() メソッド] ---
// 数値オブジェクトのメソッド。
// 注意：値が null や undefined だと「Cannot read property 'toString' of null」で即死する。
var s2 = num.toString();

console.log("Q25 s1: " + s1 + " (Type: " + typeof s1 + ")");
console.log("Q25 s2: " + s2 + " (Type: " + typeof s2 + ")");

