/**
 * Mastery Drill: No.17 (Type Conversion & parseInt Trap)
 * --------------------------------------------------
 * 【お題】文字列 "08" を確実に数値の 8 に変換せよ。
 * --------------------------------------------------
 */

var input = "08";

// --- [アンチパターン：基数省略] ---
// かつてのJSエンジンでは、先頭が0だと「8進数」と誤認されるリスクがあった。
// "08" を 0 と判定してしまうようなバグを防ぐため、基数省略は厳禁。
var riskyNum = parseInt(input); 

// --- [プロの作法：第2引数に 10 を明示] ---
// 「これは10進数ですよ」と明示するのがJSエンジニアの嗜み（儀式）。
var safeNum = parseInt(input, 10); 

console.log('Q17 変換結果: ' + safeNum); // 8

/**
 * 【ハックのポイント】
 * 1. 現場のコードレビュー:
 * エムスリーのような技術に厳しい現場では、`parseInt(str)` と書くと
 * 「第2引数の基数を指定してください」と必ずツッコミが入ります。
 * * 2. 単項プラス演算子の併用:
 * モダンな現場では `+input` と書いて数値化することもありますが、
 * 意図を明確に伝えたいバックエンド処理では `parseInt(str, 10)` が今も現役です。
 */

// おまけ：数値チェック（NaN判定）
if (isNaN(safeNum)) {
    console.error("数値への変換に失敗しました。");
}