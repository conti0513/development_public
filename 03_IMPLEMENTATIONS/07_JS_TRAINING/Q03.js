/**
 * Mastery Drill: No.03 (Arithmetic Operations)
 * --------------------------------------------------
 * 【お題】変数 x(10) と y(20) を足して結果を出力する
 * --------------------------------------------------
 */

// --- [ES5: Legacy Style] ---
var x_legacy = 10;
var y_legacy = 20;

/* 【PHP経験者への注意】
  JSの + 結合は、左側に文字列があると全体を文字列として扱います。
  'Result: ' + 10 + 20 は "Result: 1020" になるため、() が必須！
*/
console.log('ES5 Result: ' + (x_legacy + y_legacy));


// --- [ES6: Modern Style (テンプレートリテラル)] ---
const x_modern = 10;
const y_modern = 20;

/* ${} の中は「独立した計算エリア」なので、
  結合順序を気にせず安全に計算結果を埋め込めます。
*/
console.log(`ES6 Result: ${x_modern + y_modern}`);


/* ▼ 比較早見表 (Comparison Table)
  -------------------------------------------------------------
  | 機能 | ES5 (Legacy) | PHP (Reference) | ES6 (Modern) |
  | :--- | :--- | :--- | :--- |
  | 記号 | ' ' or " " | " " | ` ` (バッククォート) |
  | 結合 | + で繋ぐ | . で繋ぐ | ${ } で埋め込む |
  | 改行 | \n が必要 | \n が必要 | そのまま改行OK |
  -------------------------------------------------------------
*/

/**
 * 【解法ポイント】
 * 1. ES5（ServiceNow等）では、文字列結合時の「暗黙の型変換」による計算ミス（1020問題）を防ぐため、(x + y) と括弧で括るのが鉄則。
 * 2. モダン環境ではテンプレートリテラルを使い、可読性と安全性を両立させる。
 * 3. 商談では「環境に合わせ、演算優先順位を意識したコードが書ける」ことをアピールする。
 * 1行ハック：「JS の + は『足し算』と『文字列結合』の二面相。ES5 では (x + y) と括弧で囲み、型変換による『1020問題』を鉄壁のガードで防げ。」
 */