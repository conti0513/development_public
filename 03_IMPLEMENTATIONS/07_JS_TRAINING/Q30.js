/**
 * Mastery Drill: No.23 (Array.splice / 配列の切り取り)
 * --------------------------------------------------
 * 【シナリオ】
 * 1. リストの2番目にある要素を完全に削除せよ。
 * 2. 削除と同時に、その場所に新しい要素を差し込め。
 * --------------------------------------------------
 */

var items = ["A", "B", "C"];

// --- [基本の形: 削除] ---
// 第一引数：開始インデックス (0から数える)
// 第二引数：削除する個数
items.splice(1, 1); 

console.log("Q30 items (Bが消えた):", items); // ["A", "C"]


// --- [応用: 削除と挿入] ---
var users = ["Taro", "Jiro", "Saburo"];
// 1番目(Jiro)を消して、そこに "Shiro" を入れる
users.splice(1, 1, "Shiro");

console.log("Q30 users:", users); // ["Taro", "Shiro", "Saburo"]

