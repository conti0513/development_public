/**
 * Mastery Drill: No.23 (indexOf / 文字列の存在確認)
 * --------------------------------------------------
 * 【シナリオ】
 * ログメッセージやファイルパスの中に、特定のキーワードが含まれているか判定せよ。
 * --------------------------------------------------
 */

var msg = "Target Found";
var searchKey = "Target";

// --- [基本の形] ---
// indexOf は見つかった位置（インデックス）を返す。
// 0文字目に見つかった場合は 0 を返すため、if(msg.indexOf("Target")) と書くと
// 0 = false と判定されて「見つかっていない」扱いにされるバグが多発する。
// なので、必ず「!== -1」と比較するのが鉄則。

if (msg.indexOf(searchKey) !== -1) {
    console.log("Q23: [" + searchKey + "] が存在します。");
} else {
    console.log("Q23: 見つかりませんでした。");
}

/**
 * 【ハックのポイント：精読時の罠】
 * 1. 0の罠: 
 * if (msg.indexOf("Target")) { ... } 
 * 上記のような「!== -1」を忘れたコードを精読中に見つけたら、
 * 「先頭にキーワードがある場合に正しく動かないバグ」として指摘できます。
 * * 2. 大文字・小文字の区別:
 * indexOf は厳密です。"target" では見つかりません。
 * 現場では .toLowerCase() をかませてから indexOf するのが「デキるエンジニア」の作法。
 */