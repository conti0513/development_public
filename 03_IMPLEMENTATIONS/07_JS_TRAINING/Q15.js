/**
 * Mastery Drill: No.15 (Array Length & Safety Check)
 * --------------------------------------------------
 * 【お題】配列 list が空でないことを確認してからループを回せ。
 * --------------------------------------------------
 */

// テスト用データ（空の場合と中身がある場合）
var list = ['A', 'B', 'C'];
// var list = []; // 空配列

/**
 * 【ハックのポイント】
 * JavaScriptにおいて、空配列 `[]` は if判定で `true` になります。
 * そのため、if (list) だけでは「中身があるかどうか」は判定できません。
 */

// --- [安全な判定パターン] ---

console.log('配列の個数：' + list.length)
if (list && list.length > 0) {
    console.log("配列にデータが存在します。処理を開始します。");
    
    for (var i = 0; i < list.length; i++) {
        console.log("Index " + i + ": " + list[i]);
    }
} else {
    console.log("配列が空、または定義されていません。");
}

/* ▼ なぜ .length > 0 が必要なのか？
  -----------------------------------------------------------------------
  | 値の種類      | if (値) の結果 | 解説                                 |
  | :------------ | :------------- | :----------------------------------- |
  | `['A']`       | true           | 中身があるのでOK                      |
  | `[]` (空配列)  | **true** | **JSの仕様。器（配列）があればtrue。** |
  | `null`        | false          | 定義されていないのでOK                |
  -----------------------------------------------------------------------
*/