/**
 * Mastery Drill: No.24 (Classic For-Loop / 配列の全件処理)
 * --------------------------------------------------
 * 【シナリオ】
 * 取得したユーザーリスト（配列）を1件ずつ取り出し、
 * インデックス番号と共にログに出力せよ。
 * --------------------------------------------------
 */

var list = ["A", "B", "C"];

// --- [基本の形] ---
// 1. var i = 0 : カウンタの初期化
// 2. i < list.length : ループを続ける条件（配列の最後まで）
// 3. i++ : 1回終わるごとにカウンタを増やす
for (var i = 0; i < list.length; i++) {
    // 配列のi番目にアクセス
    console.log("Q24 " + i + ": " + list[i]);
}

